package com.restclient.client.event;

import com.google.gwt.event.shared.EventBus;
import com.google.gwt.event.shared.EventHandler;
import com.google.gwt.event.shared.GwtEvent;
import com.google.gwt.event.shared.HandlerRegistration;

/**
 * Called when user call OPEN SAVED REQUEST action by ctrl + o shortcut.
 * 
 */
public class OpenRequestEvent extends GwtEvent<OpenRequestEvent.Handler> {
	public static final Type<Handler> TYPE = new Type<Handler>();
	
	/**
	 * Register an handler for this event.
	 * @param eventBus
	 * @param handler
	 * @return
	 */
	public static HandlerRegistration register(EventBus eventBus,
			Handler handler) {
		return eventBus.addHandler(TYPE, handler);
	}

	/**
	 * Handles {@link OpenRequestEvent}.
	 */
	public interface Handler extends EventHandler {
		void onOpenAction(Object source);
	}

	public OpenRequestEvent() {
	}

	@Override
	protected void dispatch(Handler handler) {
		handler.onOpenAction(this.getSource());
	}

	@Override
	public com.google.gwt.event.shared.GwtEvent.Type<Handler> getAssociatedType() {
		return TYPE;
	}
}
